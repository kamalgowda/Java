class DataTypes {
	public static void main(String args[])
	{
		//Single line Comment
		/* Multiple line Comment
		 * 
		 * multiple
		 */

		//Primitive Data Types 


		//-128 to 127
		byte a=100;
		System.out.println(a);
		//-32768 to 32767
		short b=32767;
		System.out.println(b);
		//-2147483268 to 2147483647
		int c=2147483647;
		System.out.println(c);
		long d=148726472364278747L;
		System.out.println(d);
		float e=12.266666666878f;
		System.out.println(e);
		double f=12.63565426354767d;
		System.out.println(f);
		char g=90;
		System.out.println(g);
		boolean h=true;
		System.out.println(h);


		//Non Primitive Data Types

		String i="Kamal";
		System.out.println(i);

		String[] ka = {"kamal","Anu","Manu"};
		System.out.println(ka[1]);

		int[] kl = {10,20,30};
		System.out.println(kl[1]);
}

}